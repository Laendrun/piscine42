void	print_map(char **map, t_mapi *map_info)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < map_info->lines)
	{
		ft_putstr(map[i]);
		ft_putstr("\n");
		i++;
	}
}
