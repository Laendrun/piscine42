#include <unistd.h>
#include <stdio.h>

void	ft_putchar(char value)
{
	write(1, &value, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

int	main(int argc, char *argv[])
{
	while (argc != 1)
	{
		ft_putstr(argv[argc - 1]);
		argc--;
	}	
}
